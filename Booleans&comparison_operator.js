// Booleans & comparison operator

// booleans
// true, false

let num1 = "7"; // value is 7 but datatype is string
let num2 = 7;
// console.log(num1>=num2);

// == vs ===
console.log(num1 == num2);
// "==" srif value ka dehaan rakhega, datatype check nahi karega;

// But agr hame value k sath datatype bhi check krwana ha tu ham "===" ka use karenge;

console.log(num1 === num2);

// != vs !==

let num3 = "6";
let num4 = 6;
console.log(num3 != num4); // -- >> false

// !== (Now it also checks the datatype);
console.log(num3 !== num4) // -- >> true
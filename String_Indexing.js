// String Indexing;

let firstName = "Mohit ";

// Our Index start from 0;
//    M  o  h  i   t;
//    0  1  2  3   4 

// If we want to print particular word acc to index;
console.log(firstName[2]);

// length of string;
// if we include space in the string then it will also Count;
console.log(firstName.length);

// Last Index: length - 1;
firstName = "Mohitahsdna";
// last variable print;
console.log(firstName[firstName.length - 1]);
// second last variable print;
console.log(firstName[firstName.length - 2]);

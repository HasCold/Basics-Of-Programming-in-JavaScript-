// Rules for naming variable

// You cannot start with Number
// example:-
// 1value (invalid)
// value1 (valid)
var value1 = 3;
// we can know the square root of any number by the operator of double (**) like (3**2) = 9
console.log(value1 ** 2);

 // You can use only underscore _ or dollar Symbol in between the variable;
// firstname (valid)
// _firstname (valid)
var _Has = "Umer";
console.log(_Has);


// first$name (valid)
// $firstname (valid)
var Pho$ = "Hasan";
console.log(Pho$);

// You cannot use spaces
var first_name = "Talha";
//  if we use underscore bw the words this will called as snake case writing; 

var firstName = "Talha"; // Camel case writing;

